let score = 85;

// if-else
if (score >= 90) {
    console.log("Grade: A+");
} else if (score >= 75) {
    console.log("Grade: A");
} else {
    console.log("Grade: A-");
}

// switch
let day = 2;
switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    default:
        console.log("Invalid day");
}
